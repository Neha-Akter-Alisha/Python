# Basic Datatypes

# Integer
print(1e308)   #o/p 1e+308 #limit of integer value
print(1e309)   #o/p inf 

# Float
print(1.7e308) #o/p 1.7e+308
print(1.7e309) #o/p inf #limit of float value

# Boolean
print(True)
print(False)

# Complex
#To show complex number of a mathmetic domain
print(4+5j)

# String
#When we make application programm for user we can use  
print('Hello')      # o/p: Hello
print("Hello")      # o/p: Hello
print('''Hello''')  # o/p: Hello
print("""Hello""")  # o/p: Hello

# Container Datatypes

# List
#Basically array
print([1,2,3])

# Tuples
# List use '[]' Tuples use '()'
print((1,2,3))

# Sets
print({1,2,3},{1,2,3})

# Dict
#We use it as key:value pair in '()'
print({"Name":"Nitish","Age":30,"Gender":"Male"})  # o/p: {'Name': 'Nitish', 'Age': 30, 'Gender': 'Male'}